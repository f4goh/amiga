FINDRESIDENT=-96
OPENLIBRARY=-552
INITBITMAP=-390
INITRASTPORT=-198
SETAPEN=-342
MOVE=-240
TEXT=-60

	DC.B	"DOS",0
 	DC.L	0
	DC.L	$00000370

START:

	MOVE.L	$4,A6
	MOVEM.L	D0-D7/A0-A6,-(A7)
	BSR	DEBUT
	MOVEM.L	(A7)+,D0-D7/A0-A6

	;rts pour essai

	RTS

	LEA	DOSNAME(PC),A1
	JSR	FINDRESIDENT(A6)
	MOVE.L	D0,A0
	TST.L	D0
	BEQ.S	NO_DOS
	MOVE.L	$16(A0),A0
	MOVEQ	#0,D0

START_END:

	RTS

NO_DOS:
	MOVEQ	#-1,D0
	BRA.S	START_END

DEBUT:

	LEA	GFXNAME(PC),A1
	CLR.L	D0
	JSR	OPENLIBRARY(A6)
	MOVE.L	D0,A6
	LEA	NEWCOPPER(PC),A1
	LEA	$52000,A2

REMPLITCOPPER:

	CMPI.L	#0,(A1)
	BEQ	COPPER
	MOVE.L	(A1)+,(A2)+
	BRA	REMPLITCOPPER

COPPER:

	MOVE	#280,D0
	MOVE	#$20E1,D1
BOUCLE:	MOVE	D1,(A2)+
	MOVE	#$FFFE,(A2)+
	MOVE	#$180,(A2)+
	ADD.L	#2,A2
	ADD	#$100,D1
	DBRA	D0,BOUCLE
	MOVE.L	#$FFFFFFFE,(A2)
	BSR	INIT
	MOVE.W	#$A0,$DFF096
	MOVE.L	$32(A6),$51000
	MOVE.L	#$52000,$32(A6)
	MOVE.W	#$8380,$DFF096
	LEA	PTCOULEURS(PC),A0
	LEA	COULEURS(PC),A1
	MOVE.L	A1,(A0)

PGMBOOT:

	CMP.B	#200,$DFF006
	BNE	PGMBOOT
	BSR	TUBES
	BTST	#6,$BFE001
	BNE.S	PGMBOOT
	MOVE.W	#$80,$DFF096
	MOVE.L	$51000,$32(A6)
	MOVE.W	#$80A0,$DFF096
	MOVE.L	4,A6
	RTS

INIT:

	LEA	$70000,A1

EFFACEBITMAP:

	CLR.L	(A1)+
	CMPA.L	#$7E000,A1
	BNE	EFFACEBITMAP
	LEA	$41000,A0
	MOVE.L	#1,D0
	MOVE.L	#$300,D1
	MOVE.L	#$C0,D2
	JSR	INITBITMAP(A6)
	MOVE.L	#$70000,$41008
	LEA	$41100,A1
	JSR	INITRASTPORT(A6)
	MOVE.L	#$41000,$41104
	LEA	$41100,A1
	MOVE.W	#1,D0
	JSR	SETAPEN(A6)
	LEA	TEXTE(PC),A5

PHRASE_SUIV:

	LEA	$41100,A1
	CLR.L	D4
	MOVE.L	#$32,D3
	MOVE.B	(A5)+,D4
	BEQ	RETOUR
	BSR	ECRITURE
	ADDI.L	#$180,D3
	BSR	ECRITURE
	ADDA.L	#$1F,A5
	BRA	PHRASE_SUIV
RETOUR:	RTS

ECRITURE:
	
	LEA	$41100,A1
	MOVE.L	D3,D0
	MOVE.L	D4,D1
	JSR	MOVE(A6)
	LEA	$41100,A1
	MOVE.L	A5,A0
	MOVE.L	#$1F,D0
	JSR	TEXT(A6)
	RTS

TUBES:

	LEA	$52000+64+6,A0
	MOVE	#279,D0
	LEA	COULEURS(PC),A4
	LEA	PTCOULEURS(PC),A2
	MOVE.L	PTCOULEURS(PC),A3
	CMP.L	A2,A3
	BNE	PASFIN
	MOVE.L	A4,(A2)
	
PASFIN:	MOVE.L	PTCOULEURS(PC),A1

ON_REMPLIT:

	MOVE	(A1)+,(A0)
	ADD.L	#8,A0
	CMP.L	A2,A1
	BNE	PASFIN2
	MOVE.L	A4,A1
	
PASFIN2:

	DBRA	D0,ON_REMPLIT
	ADD.L	#2,(A2)
	RTS

COULEURS:

	DC.W	$000,$111,$222,$333
	DC.W	$444,$555,$666,$777
	DC.W	$888,$999,$AAA,$BBB
	DC.W	$CCC,$DDD,$DDD,$CCC
	DC.W	$BBB

PTCOULEURS:

	DC.L	0
TEXTE:
	DC.B	40,"         HALL OF FAME          "
	DC.B	50,"                               "
	DC.B	60,"           PRESENTE            "
	DC.B	70,"                               "
	DC.B	80,"        LE BOOTER FOU          "
	DC.B	90," ALE,EXODUS,JAGUAR,THUNDERBOLT "
	DC.B	100," ----------------------------- "
	DC.B	110,"                               "
	DC.B 	120,"                               "
	DC.B	140," PRESSER LE BOUTON GAUCHE SVP  ",0,0

NEWCOPPER:

	DC.W	$0100,$2200,$0102
	DC.W	$0001,$0108,$0000
	DC.W	$010A,$0000,$0092
	DC.W	$0020,$0094,$00D8
	DC.W	$008E,$3000,$0090
	DC.W	$30FF,$00E0,$0007
	DC.W	$00E2,$0870,$00E4
	DC.W	$007,$00E6,$08A0
	DC.W	$0180,$0000,$0182
	DC.W	$000F,$0184,$0000
	DC.W	$0186,$0009,0,0

DOSNAME:
	
	DC.B	"DOS.LIBRARY",0

GFXNAME:

	DC.B	"GRAPHICS.LIBRARY",0
	BLK.B	1024,0

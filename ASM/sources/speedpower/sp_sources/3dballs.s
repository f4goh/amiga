	;PRIORITE DES PLANS ET SPRITES - INUTILISE ICI

	DC.W 	$0104,$0000

	;MODULOS DES PLANS

	DC.W	$0108,6+46-4	;MODULOS PLANS PAIRS    OVERSCAN

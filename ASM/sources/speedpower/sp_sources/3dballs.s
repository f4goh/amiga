;NE COMPORTE PAS LA COMMANDE Y

>EXTERN "BOULES_RAW",BOULES

org $30000	;EMPLACEMENT EN MEMOIRE
load $30000	;DE NOTRE PROGRAMME

BEGIN:
JMP START
;=====================================================================
EXECBASE=4
OPENLIBRARY=-408
CLOSELIBRARY=-414
SUPERSTATE=-150
USERSTATE=-156	
FORBID=-132
PERMIT=-138
ADDINTSERVER=-168
REMINTSERVER=-174
Open=-30
Close=-36
Read=-42
;=====================
STARSNBR        = 40	;NOMBRE DE BOULES
STARSSPEED      = 2	;VITESSE DES BOULES
;=====================================================================
BLTCON0=$DFF040		
BLTCON1=$DFF042
BLTAFWM=$DFF044
BLTALWM=$DFF046
BLTCPTH=$DFF048
BLTCPTL=$DFF04A
BLTBPTH=$DFF04C
BLTBPTL=$DFF04E
BLTAPTH=$DFF050
BLTAPTL=$DFF052
BLTDPTH=$DFF054
BLTDPTL=$DFF056
BLTSIZE=$DFF058
BLTCMOD=$DFF060
BLTBMOD=$DFF062
BLTAMOD=$DFF064
BLTDMOD=$DFF066
BLTCDAT=$DFF070
BLTBDAT=$DFF072
BLTADAT=$DFF074
DMACONR=$DFF002
;==================================
BOULES=$40000			;IMAGE DES BOULES
				;320 SUR 18 EN 2 PLANS
ECRAN=$50000			;ECRAN DOUBLE
				;368 SUR 150 EN 2 PLANS   2 FOIS
COORDS=ECRAN+[46*150*4]		;EMPLACEMENT DES COORDONNEES
				
;==================================
START:    
MOVEM.L D0-D7/A0-A6,-(A7)	;SAUVE REGISTRES
MOVE.L EXECBASE,A6		;CHARGE ADRESSE EXECBASE DANS A6
JSR FORBID(A6)    		;SAUTE SOUS PROGRAMME: INTERDIT MULTITACHE
JSR OPENLIB  			;SAUTE SOUS PROGRAMME: OUVERTURE LIBRAIRIES

LEA ECRAN,A0			;EFFACEMENT EMPLACEMENT ECRANS
MOVE.W #[46*150*2]-1,D0
EFFACE:
MOVE.W #0,(A0)+
DBF D0,EFFACE

BSR.L	STARSINIT		;INITIALISATION DES DES COORDONNES DES BOULES

BSR.L SETCOPPER			;SAUTE SOUS PROGRAMME: MET EN PLACE LISTE COPPER
BSR.s NEWIRQ3			;SAUTE SOUS PROGRAMME: MET EN PLACE ROUTINE D'INTERRRUPTION

WAIT:
CMP.B #$20,$DFF006		;ATTENDANT 200 IEME LIGNE RASTER
BNE.S WAIT			;SINON RETOURNE A WAIT

BSR.L	STARS			;VA ROUTINE D'AFFICHAGE DES BOULES

BTST	#6,$BFE001		;ATTENDANT PRESSION SUR BOUTON GAUCHE SOURIS
BNE.S	WAIT			;SINON RETOURNE A WAIT
 				;SI OUI  FIN
END: 				;FIN:	
BSR.s STOPIRQ3        		;SAUTE SOUS PROGRAMME: STOPPE INTERRUPTION
BSR.L CLRCOPPER       		;SAUTE SOUS PROGRAMME: RESTAURE ANCIENNE LISTE COPPER
BSR.L CLOSELIB        		;SAUTE SOUS PROGRAMME: FERME LIBRAIRIES 
MOVE.L EXECBASE,A6		;CHARGE ADRESSE EXECBASE DANS A6
JSR PERMIT(A6)      		;AUTORISE MULTITACHE
                      
MOVEM.L (A7)+,D0-D7/A0-A6	;RESTAURE REGISTRES
RTS
;========================
NEWIRQ3:			;MET EN PLACE NOTRE ROUTINE 
MOVE.L	EXECBASE,A6		;D'INTERRUPTION
lea	irqstr,a1
move.l	#5,d0
jsr	addintserver(a6)
move.l	d0,irq
beq.s	irqerror
rts
irqerror:
rts
;==========================
STOPIRQ3:			;RESTAURE ANCIENNE ROUTINE
movem.l	a0-a6/d0-d7,-(sp)	;D'INTERRUPTION
	tst.l	irq
	beq.s	irqoff1
	move.l	execbase,a6
	lea	irqstr,a1
	move.l	#32,d0
	jsr	remintserver(a6)
irqoff1:movem.l	(sp)+,a0-a6/d0-d7
	rts
irqstr:
	dc.l	0,0
	dc.b	2,-127
	dc.l	0,irqdat,interrupt
irqdat:	dc.l	0,0,0,0
irq:	dc.l	0
;============================
interrupt:			;NOTRE ROUTINE D'INTERRUPTION	
movem.l	a0-a6/d0-d7,-(sp)

	
movem.l	(sp)+,a0-a6/d0-d7
rts
;============================
OPENLIB:  			;OUVERTURE DE LA LIBRAIRIE GRAPHIQUE
move.l execbase,a6      	;MET ADRESSE EXECBASE DANS A6
lea gfxname,a1			;MET NOM DE LA LIBRAIRIE DANS A1
move.l #$0,d0           	;MET 0 DANS D0
jsr openlibrary(a6)		;OUVRE LIBRAIRIE
tst d0				;TESTE D0
beq.s error			;SI = A 0   VA A ERROR = SORTIE
move.l d0,gfxbase       	;SI NON, MET ADRESSE D0 DANS GFXBASE
add.l #$32,d0			;AJOUTE $32 A CETTE ADRESSE
move.l d0,coppertr      	;MET D0=ADRESSE ACTUELLE LISTE COPPER
rts				;DANS COPPERTR
even
gfxname: dc.b 'graphics.library',0 	;NOM DE LA LIBRAIRIE
even
gfxbase: dc.l 0				;POINTEUR POUR LA GRAPHICS LIBRARY
coppertr: dc.l 0			;POINTEUR POUR ADRESSE LISTE COPPER
;====================
CLOSELIB: 			;FERMETURE LIBRAIRIE
move.l execbase,a6      	;MET ADRESSE EXECBASE DANS A6
move.l gfxbase,a1		;MET NOM DE LA LIBRAIRIE DANS A1
jsr closelibrary(a6)		;FERME LIBRAIRIE
rts
;====================
ERROR:				;SI ERREUR
illegal				;SORTIE
rts
;====================
SETCOPPER:			;MET EN PLACE NOTRE LISTE COPPER
move.l coppertr,a0		;MET ADRESSE ACTUEL COPPER DANS A0
move.l (a0),oldcopper		;MET CONTENU DANS OLDCOPPER
move.l #newcopper,(a0)		;MET NOTRE LISTE COPPER DANS A0
move.w #$81c0,$dff096		;ACTIVE LISTE COPPER
rts		
oldcopper: dc.l 0
;====================
CLRCOPPER:			;RESTAURE ANCIENNE LISTE COPPER
move.l coppertr,a0		;MET ANCIENNE ADRESSE DANS A0
move.l oldcopper,(a0)		;MET CONTENU DANS A0
move.w #$80a0,$dff096		;REACTIVE ANCIENNE COPPER
rts
;====================
WAITBLITTER:			;ROUTINE POUR ATTENDRE QUE LE BLITTER
BTST #14,DMACONR		;EST FINI SON PRECEDENT TRAVAIL
BNE.S WAITBLITTER
RTS
;===================
;; ---- SOUS-PROGRAMMES ----

					;ECRAN 1 A $50000
					;ECRAN 2 A $50000+[46*150*2]
PLFDCHANGE2:	DC.W	0		;MOT DE POIDS FAIBLE DU 1ER ECRAN
		DC.W	50*133*2	;MOT DE POIDS FAIBLE DU 2EME ECRAN


;; ---- STARS Routine ----

STARS:

COORDSCALC2:
	LEA	PLFD2+6,A1	;POINTE A1 SUR LISTE COPPER ECRAN 
	LEA	PLFD3+6,A2	;POINTE A2 SUR LISTE COPPER ECRAN INVERSE

	LEA	PLFDCHANGE2,A3	;POINTE A3 SUR TABLE MOT POIDS FAIBLE

	MOVE.W	(A3),D0		;MET EN PLACE ADRESSE DES ECRANS VISIBLES
	MOVE.W	D0,(A1)		;DANS LISTE COPPER

	MOVE.W D0,D1		;POUR L'ECRAN INVERSE
	ADD.W #130*46*2,D1	;ON DOIT POINTER SUR LA DERNIERE LIGNE
	MOVE.W D1,(A2)

	ADD.W	#46,D0
	MOVE.W	D0,8(A1)

	MOVE.W D0,D1
	ADD.W #130*46*2,D1
	MOVE.W D1,8(A2)

	MOVE.L	(A3),D0
	SWAP	D0
	MOVE.L	D0,(A3)


	BSR	WAITBLITTER	;EFFACE ECRAN NON VISIBLE
	MOVE.W	#$0100,BLTCON0
	MOVE.W	#0,BLTCON1
	MOVE.W	#0,BLTDMOD
	MOVE.L	#ECRAN,D1
	ADD.W	PLFDCHANGE2,D1
	MOVE.L	D1,BLTDPTH
	MOVE.W	#133*2*64+23,BLTSIZE

	BSR	WAITBLITTER	;PREPARE BLITTER POUR AFFICHAGE BOULES
	MOVE.W	#0,BLTCON1
	MOVE.W	#$FFFF,BLTAFWM
	MOVE.W	#$0000,BLTALWM
	MOVE.W	#20-1-1*2,BLTAMOD
	MOVE.W	#23-1-1*2,BLTBMOD
	MOVE.W	#23-1-1*2,BLTDMOD

	LEA	COORDS,A0		;CALCUL DES ADRESSES
	MOVE.W	#STARSNBR-1,D0		;ET AFFICHAGE DES BOULES

STARSLOOP:
	MOVE.L	A0,-(SP)

	SUBQ.W	#STARSSPEED,4(A0)
	MOVE.W	4(A0),D4
	BLS.L	ROUT2

	MOVE.W	(A0)+,D2
	EXT.L	D2
	DIVS	D4,D2
	MOVE.W	(A0)+,D3
	TST.W	(A0)+
	EXT.L	D3
	DIVS	D4,D3

	ADD.W	#176,D2		; CENTRER BORIZONTALEMENT
	BMI.L	ROUT2		; TESTER BORD GAUCHE

	CMP.W	#352,D2		;   --    --  DROIT
	BGE.L	ROUT2

	ADD.W	#124,D3		; CENTRER VERTICALEMENT
	BMI.L	ROUT2

	MULU	#46*2,D3	; ADRESSE DE LA LIGNE (44 OCTETS/LIGNE)

	MOVE.W	D2,D1
	LSR.W	#3,D1		; ADRESSE DANS LA LIGNE

	ADD.W	D1,D3		; ADRESSE FINALE

	TST.W	(A0)+
	MOVE.W	D3,(A0)+

	AND.W	#$F,D2
	ROR.W	#4,D2
	OR.W	#$0DFC,D2

	MOVE.L	#BOULES,D6

	MULU	#12,D4
	LSR.W	#7,D4
	AND.L	#$0000FFFE,D4

FINALADDRESS:
	ADD.L	D4,D6

	LEA	BALLSHEIGHT,A3
	ADD.L	D4,A3

	MOVE.L	#0,D7
	MOVE.W	(A3),D7
	LSL.L	#7,D7
	OR.W	#2,D7
	
	BSR	WAITBLITTER

	MOVE.W	D2,BLTCON0

	MOVE.L	#ECRAN,D5
	ADD.W	PLFDCHANGE2,D5
	ADD.W	D3,D5

	MOVE.L	D6,BLTAPTH
	MOVE.L	D5,BLTBPTH
	MOVE.L	D5,BLTDPTH
	MOVE.W	D7,BLTSIZE
	TST.L	(SP)+

STARSEND:
	DBRA	D0,STARSLOOP

	RTS
 

ROUT2:				;ROUTINE DE CALCUL ALEATOIRE
	MOVE.W	#1000,D7
	MOVE.L	(SP)+,A0
	MOVE.W	#300,4(A0)
	MOVE.B	$DFF007,D5
	MOVE.B	$BFD800,D4	; ENVENT COUNTER DU CIA-B (COMPTE LES
	EOR.B	D5,D4		; IMPULSIONS SYNCHRONES HORIZONTALES,
	EXT.W	D4		; EN TEMPS NORMAL, ELLES ONT UNE 
	ASL.W	#6,D4		; FRéQUENCE DE 15625 PAR SECONDE)
	BMI.S	L1
	ADD.W	D7,D4
	BRA.S	L2
 
L1:	SUB.W	D7,D4

L2:	MOVE.W	D4,(A0)+
	MOVE.B	$BFE801,D4	; COMPTEUR éVéNEMENT (COMPTE LES
	EOR.B	D4,D5		; IMPULSIONS ŕ 50HZ DU COMPTEUR
	EXT.W	D5		; D'ALIMENTATION, QUI DéRIVE DE
	ASL.W	#6,D5		; LA FRéQUENCE DU RéSEAU)
	BMI.S	L3
	NEG.W	D5

L3:	SUB.W	D7,D5
	MOVE.W	D5,(A0)+
	TST.W	(A0)+
	CLR.W	(A0)+
	CLR.W	(A0)+
	BRA.S	STARSEND

;; ---- STARS INIT ROUTINE ----

STARSINIT:			;INITIALISATION DES COORDONNEES DES BOULES
	LEA	COORDS,A0
	MOVE.L	#STARSNBR-1,D0

STARSINITLOOP1:
	MOVE.B	$00DFF007,D5
	MOVE.L	#0,D4
	MOVE.B	$00BFD800,D4
	MOVE.W	D4,4(A0)
	EOR.B	D5,D4
	ASL.W	#6,D4
	MOVE.W	D4,(A0)+
	AND.W	#$00FF,D4

STARSINITLOOP2:
	DBRA	D4,STARSINITLOOP2
	MOVE.B	$00BFE801,D4
	EOR.B	D4,D5
	ASL.W	#6,D5
	BSET	#15,D5
	MOVE.W	D5,(A0)+
	TST.W	(A0)+
	CLR.W	(A0)+
	CLR.W	(A0)+
	DBRA	D0,STARSINITLOOP1
	RTS


BALLSHEIGHT:	;TAILLE DES BOULES EN PIXELS

DC.W	17,17,16,15,14,13,12,11,10,8,7,6,5,4,2,1,1


;====================
EVEN				;NOTRE LISTE COPPER
